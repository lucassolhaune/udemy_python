#Int
x = 5
print(type(x))
print(x)


#Float
x = 7.1
print(type(x))
print(x)


#Bool
es_verdadero = True
print(type(es_verdadero))
print(es_verdadero)


#String
saludo = "Hola, Lucas"
print(type(saludo))
print(saludo)


#Complex
x = 2 + 3j
print(type(x))
print(x)


