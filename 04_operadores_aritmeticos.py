#########################################
##Operadores

#Suma
suma = 3 + 4
print("Suma: ", suma)

#Resta
resta = 4 - 3
print("Resta: ", resta)

#Multiplicacion
multp = 3 * 4
print("Multiplicacion: ", multp)

#Division con flotante
div = 12 / 4
print("Division con flotante: ", div)

#Division (entera), sin decimales
div1 = 56 // 5
print("Division entera: ", div1) 

#Potencia
pot = 3 ** 4
print("Potencia: ", pot)

#Modulo (resto de la division)
mod = 10 % 3
print("Modulo: ", mod)



